    public boolean checktrung(String ma) {
        for (int i = 0; i < list.size(); i++) {
            if (list.get(i).MaSach.equalsIgnoreCase(ma)) {
                return true;
            }
        }
        return false;
    }
    private void btnluuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnluuActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder(); // để ghi thông báo nếu để rỗng giá trị hoặc không chọn
        if (txtmaSach.getText().isBlank()) {
            sb.append("Bạn cần nhập mã sách\n");
        }
        if (txttenSach.getText().isBlank()) {
            sb.append("Bạn cần nhập tên sách\n");
        }
        if (txttacGia.getText().isBlank()) {
            sb.append("Bạn cần nhập tác giả sách\n");
        }
        if (txttheLoai.getText().isBlank()) {
            sb.append("Bạn cần nhập thể loại sách\n");
        }
        if (txttinhTrang.getText().isBlank()) {
            sb.append("Bạn cần nhập tình trạng sách\n");
        }
        if (txtsoLuong.getText().isBlank()) {
            sb.append("Bạn cần nhập số lượng sách\n");
        }

        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(null, sb);//nếu sb(đoạn cảnh báo) có độ dài thì xuất hiện bảng loại messa có câu cảnh báo
        } else {
            if (checktrung(txtmaSach.getText()) == true) {
                JOptionPane.showMessageDialog(this, "Mã sách bị trùng");
            } else {
                int row = 0;
                try {

                    Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                    Connection conn = DriverManager.getConnection(url, userName, password);
                    String sql = "insert into QLSACH values (?,?,?,?,?,?)";
                    PreparedStatement st = conn.prepareStatement(sql);
                    st.setString(1, txtmaSach.getText());
                    st.setString(2, txttenSach.getText());
                    st.setString(3, txttheLoai.getText());
                    st.setString(4, txttacGia.getText());
                    st.setString(5, txtsoLuong.getText());
                    st.setString(6, txttinhTrang.getText());

                    row = st.executeUpdate();
                    if (row > 0) {

                        JOptionPane.showMessageDialog(this, "Lưu thành công");
                    } else {
                        JOptionPane.showMessageDialog(this, "Trùng mã sách");
                    }
                    conn.close();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Lỗi");
                    System.out.println(e);
                }
            }
        }
        LoadDataToArray();
        LoadDataArrayListToTable();
    }//GEN-LAST:event_btnluuActionPerformed

