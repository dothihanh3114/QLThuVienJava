    private void timtheoMaSach() {
        String maSach = txtTim.getText();
        DefaultTableModel model = (DefaultTableModel) tblSachs.getModel();
        model.setRowCount(0);
        boolean tim = false;

        for (sachs book : list) {
            if (book.getMaSach().toLowerCase().contains(maSach.toLowerCase())) {
                model.addRow(new Object[]{book.getMaSach(), book.getTenSach(), book.getTheLoai(), book.getTacGia(), book.getSoLuong(), book.getTinhTrang()});
                tim = true;
            }
        }
        if (!tim) {
            JOptionPane.showMessageDialog(this, "Mã sách khong tồn tại");
        }
    }
